package it.alessandro.Somma;

public class Somma {
	double valore1;
	double valore2;

	
	public String toString() {
		return "Valore 1 = " + valore1 + " Valore 2 = " + valore2 + " Somma: " + (valore1 + valore2); 
	}


	public double getValore1() {
		return valore1;
	}


	public void setValore1(double valore1) {
		this.valore1 = valore1;
	}


	public double getValore2() {
		return valore2;
	}


	public void setValore2(double valore2) {
		this.valore2 = valore2;
	}


}
